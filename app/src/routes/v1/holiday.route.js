const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');

const holidayValidation = require('../../validations/holiday.validation');
const holidayController = require('../../controllers/holiday.controller');

const router = express.Router();

router
  .route('/')
  .get(auth('getAll:holiday'), validate(holidayValidation.getHolidays), holidayController.getHolidays)
  .post(auth('create:holiday'), validate(holidayValidation.createHoliday), holidayController.createHoliday);

router
  .route('/:id')
  .get(auth('get:holiday'), validate(holidayValidation.getHoliday), holidayController.getHoliday)
  .patch(auth('update:holiday'), validate(holidayValidation.updateHoliday), holidayController.updateHoliday)
  .delete(auth('delete:holiday'), validate(holidayValidation.deleteHoliday), holidayController.deleteHoliday);

module.exports = router;

